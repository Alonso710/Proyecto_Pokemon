     // Habilita los botones de ataque al usuario, segun su turno pasando su estado como un boolean
   
    public void is_Atacando(boolean Estado) {
        if (Estado)
            lbl_Estado.setText("Listo para atacar.");
        else
            lbl_Estado.setText("Esperando al oponente.");
    }
    
    private void Habilidar_Skills(boolean Estado)
    {
        btn_S1.setVisible(Estado);
        btn_S2.setVisible(Estado);
        btn_S3.setVisible(Estado);
        btn_S4.setVisible(Estado);
        
        btn_Atacar.setVisible(false);
    }
    
    
    
     
    
